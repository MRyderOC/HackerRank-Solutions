from itertools import accumulate
def equalStacks(h1, h2, h3):
    tmp1 = set(accumulate(h1[::-1]))
    tmp2 = set(accumulate(h2[::-1]))
    tmp3 = set(accumulate(h3[::-1]))
    intersections = tmp1.intersection(tmp2).intersection(tmp3)
    if intersections:
        return max(intersections)
    else:
        return 0
    
    
    
    
    

