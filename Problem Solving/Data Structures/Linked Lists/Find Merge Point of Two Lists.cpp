    SinglyLinkedListNode *tmp1 = head1;
    SinglyLinkedListNode *tmp2 = head2;
    while (tmp1 != nullptr && tmp2 != nullptr) {
        tmp1 = tmp1->next;
        tmp2 = tmp2->next;
    }

    // Define related variables and assign them the right values
    bool equal_length = false;
    SinglyLinkedListNode *long_list;
    SinglyLinkedListNode *short_list;
    SinglyLinkedListNode *remaining;
    if (tmp1 == nullptr && tmp2 == nullptr) {
        equal_length = true;
        short_list = head2;
        long_list = head1;
    } else if (tmp1 == nullptr) {
        short_list = head1;
        long_list = head2;
        remaining = tmp2;
    } else if (tmp2 == nullptr) {
        short_list = head2;
        long_list = head1;
        remaining = tmp1;
    }

    // Put pointers at the right place
    if (!equal_length)
        while (remaining != nullptr) {
            remaining = remaining->next;
            long_list = long_list->next;
        }

    // Find the merge point
    while (short_list != long_list) {
        short_list = short_list->next;
        long_list = long_list->next;
    }

